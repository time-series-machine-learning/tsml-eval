from tsml_eval.evaluation.storage.forecaster_results import ForecasterResults
from tsml_eval.utils.results_loading import _load_by_problem_init
import numpy as np
from datetime import datetime
from sklearn.metrics import (
    mean_absolute_percentage_error,
)

from tsml_eval.utils.results_writing import write_forecasting_results

def load_forecaster_results(
    forecaster_names,
    path = "../ForecastingResults/master_comparison",
):
    """
    Evaluate multiple forecasters on multiple datasets from file using standard paths.

    Finds files using forecaster, dataset and resample names. It is expected the
    common tsml-eval file structure of
    {forecaster}/Predictions/{dataset}/{split}Resample{resample}.csv is followed.

    Writes multiple csv files and figures to save_path, one for each statistic
    evaluated. Provides a summary csv file with the average statistic and
    average rank for each forecaster.

    Parameters
    ----------
    forecaster_names : list of str, list of tuple or list of list
        The names of the forecasters to evaluate.
        A length 2 tuple containing strings can be used to specify a forecaster name to
        load from in the first item and a forecaster name to use in the evaluation
        results in the second.
        If load_path is a list, regressor_names must be a list of lists with the same
        length as load_path.
    """
    dataset_file = "../ForecastingResults/Data/windowed_series.txt"
    load_path, forecaster_names_array, dataset_names, resamples = _load_by_problem_init(
        "forecaster",
        path,
        forecaster_names,
        dataset_file,
        1,
    )
    forecaster_datasets : dict[str, dict[str, ForecasterResults]] = {}
    path = load_path[0]
    for dataset_name in dataset_names[0]:
        forecaster_results : dict[str, ForecasterResults] = {}
        for forecaster_name in forecaster_names_array[0]:
            try:
                result = ForecasterResults().load_from_file(
                    f"{path}/{forecaster_name}/Predictions/"
                    f"{dataset_name}/testResample{resamples[0]}.csv",
                    verify_values=False,
                )
                forecaster_results[forecaster_name] = result
                if result.dataset_name != dataset_name:
                    result.dataset_name = dataset_name
            except FileNotFoundError:
                msg = (
                    f"Results for {forecaster_name} on {dataset_name} not found."
                )
                raise FileNotFoundError(msg)
        forecaster_datasets[dataset_name] = forecaster_results

    return forecaster_datasets

def write_ensemble_results(predictions, labels, ensemble_name, ensemble_type, forecaster_list, dataset_name, path="../ForecastingResults/generated_ensembles"):
    first_comment = (
        "Generated by ensemble_forecasters on "
        f"{datetime.now().strftime('%m/%d/%Y, %H:%M:%S')}"
    )
    second = f"Ensemble Forecaster type: {ensemble_type}, Forecasters: {', '.join(forecaster_list)}"
    test_mape = mean_absolute_percentage_error(labels, predictions)
    write_forecasting_results(
        predictions,
        labels,
        ensemble_name,
        dataset_name,
        file_path=path,
        full_path=False,
        first_line_forecaster_name=f"{ensemble_name}",
        split="TEST",
        random_seed=0,
        time_unit="MILLISECONDS",
        first_line_comment=first_comment,
        parameter_info=second,
        mape=test_mape,
        fit_time=0,
        predict_time=0,
        benchmark_time=0,
        memory_usage=0,
    )

def mean_ensemble(forecaster_results):
    mean_predictions = None
    for forecaster, result in forecaster_results.items():
        if mean_predictions is None:
            mean_predictions = result.predictions
        else:
            mean_predictions += result.predictions
    mean_predictions /= len(forecaster_results)
    return mean_predictions, "SimpleEnsemble", "mean"

def median_ensemble(forecaster_results):
    # Collect all predictions into a list
    predictions_list = [result.predictions for result in forecaster_results.values()]
    
    # Compute the element-wise median across all forecasters (axis 0)
    median_predictions = np.median(predictions_list, axis=0)
    
    return median_predictions, "MedianEnsemble", "median"

def hierarchical_ensemble(forecaster_results):
    intermediate_results = {"scum" : [], "ml": []}
    intermediate_results_num = {"scum": 0, "ml": 0}
    forecasters = {"SCUM" : "scum", "RandomForestRegressor_refit": "ml", "Ridge_refit": "ml", "xgboost_refit": "ml", "DDrCIFRegressor_refit": "ml"}
    for forecaster, result in forecaster_results.items():
        type = forecasters[forecaster]
        intermediate_results[type].append(result.predictions)
        intermediate_results_num[type] += 1
    for type in intermediate_results.keys():
        intermediate_results[type] = np.median(intermediate_results[type], axis=0)
    final_predictions = np.median([value for key, value in intermediate_results.items()], axis=0)
    return final_predictions, "SyntheticHierarchicalSCUMML", "hierarchicalmedian"

def do_ensemble(method):
    list_of_forecasters = [
            "SCUM",
            "RandomForestRegressor_refit",
            "Ridge_refit",
            "xgboost_refit",
            "DDrCIFRegressor_refit",
        ]
    forecaster_datasets = load_forecaster_results(list_of_forecasters)

    for dataset, forecaster_results in forecaster_datasets.items():
        predictions, name, type = method(forecaster_results)

        write_ensemble_results(
            predictions,
            list(forecaster_results.items())[0][1].target_labels,
            f"{name}{''.join([name for name in list_of_forecasters])}",
            type,
            list_of_forecasters,
            dataset
        )

if __name__ == "__main__":
    do_ensemble(hierarchical_ensemble)