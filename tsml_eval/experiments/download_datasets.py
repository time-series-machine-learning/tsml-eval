import os
import sys

from aeon.datasets import load_forecasting
from aeon.datasets import load_from_tsf_file

# Seasonal period (primary cycle length) for each Monash .tsf @frequency value. These are
# the "natural" seasonalities used in the Monash TSF archive (Godahewa et al.): the M4
# frequencies resolve to hourly=24, daily=7, weekly=52, monthly=12, quarterly=4,
# yearly=1. Edit this map if a different convention is required.
FREQUENCY_TO_SEASONAL_PERIOD = {
    "yearly": 1,
    "quarterly": 4,
    "monthly": 12,
    "weekly": 52,
    "daily": 7,
    "hourly": 24,
    "half_hourly": 48,
    "10_minutes": 144,
    "minutely": 1440,
}

# Fallback used when the .tsf @frequency metadata is missing or unrecognised: infer the
# frequency from a keyword in the dataset name (e.g. "m4_monthly_dataset" -> monthly).
_NAME_KEYWORDS = [
    ("half_hourly", "half_hourly"),
    ("10_minutes", "10_minutes"),
    ("minutely", "minutely"),
    ("quarterly", "quarterly"),
    ("monthly", "monthly"),
    ("weekly", "weekly"),
    ("hourly", "hourly"),
    ("daily", "daily"),
    ("yearly", "yearly"),
]


def _seasonal_period_for(dataset_name, metadata):
    """Resolve the seasonal period for a dataset from its .tsf frequency."""
    frequency = (metadata.get("frequency") or "").strip().lower()
    if frequency in FREQUENCY_TO_SEASONAL_PERIOD:
        return FREQUENCY_TO_SEASONAL_PERIOD[frequency]

    lowered = dataset_name.lower()
    for keyword, frequency_key in _NAME_KEYWORDS:
        if keyword in lowered:
            return FREQUENCY_TO_SEASONAL_PERIOD[frequency_key]

    print(
        f"WARNING: could not determine seasonal period for {dataset_name} "
        f"(frequency={frequency!r}); defaulting to 1."
    )
    return 1


def check_and_download_datasets(args):
    data_path = args[0]
    dataset_file_path = args[1]
    list_of_series_file = args[2]
    if "_dataset.txt" in dataset_file_path:
        # Alongside the compiled series list, write a per-series seasonal period file
        # (<series_name> <seasonal_period> per line, derived from the .tsf @frequency
        # metadata). The cluster scripts' get_seasonal_period reads this file when the
        # config sets relative_seasonal_period_file="<dataset list>_seasonal_periods.txt".
        # Needed for dataset lists mixing frequencies (e.g. M4), where a single fixed
        # seasonal_period in the config is not enough.
        seasonal_period_file_path = f"{dataset_file_path}_seasonal_periods.txt"
        with open(dataset_file_path, 'r') as dataset_list, \
                open(list_of_series_file, 'w') as series_file, \
                open(seasonal_period_file_path, 'w') as seasonal_file:
            seasonal_file.write("# Seasonal periods generated by download_datasets.py.\n")
            seasonal_file.write("# Format: <series_name> <seasonal_period>\n")
            seasonal_file.write(
                "# Derived from the Monash .tsf @frequency metadata: "
                "yearly=1, quarterly=4, monthly=12, weekly=52, daily=7, hourly=24.\n"
            )
            lines = dataset_list.readlines()
            for dataset_name in lines:
                dataset_name = dataset_name.strip()
                data_full_path = f"{data_path}/{dataset_name}/{dataset_name}.tsf"
                if not os.path.exists(data_full_path):
                    print(f"Downloading {dataset_name}")
                    load_forecasting(dataset_name, data_path)
                df, metadata = load_from_tsf_file(data_full_path)
                series_list = [f"{dataset_name}_{series}\n" for series in df["series_name"].tolist()]
                series_file.writelines(series_list)
                period = _seasonal_period_for(dataset_name, metadata)
                print(f"{dataset_name}: seasonal_period={period} ({len(df)} series)")
                seasonal_file.writelines(
                    f"{dataset_name}_{series} {period}\n"
                    for series in df["series_name"].tolist()
                )
    else:
        with open(dataset_file_path, 'r') as dataset_file:
            lines = dataset_file.readlines()
            for dataset_name in lines:
                if "_" in dataset_name:
                    dataset, _ = dataset_name.rsplit("_", 1)
                else:
                    raise ValueError(f"Dataset {dataset_name} given, but has no series attached when remote_forecasting_experiment called.")
                data_full_path = f"{data_path}/{dataset}/{dataset}.tsf"
                if not os.path.exists(data_full_path):
                    print(f"Downloading {dataset}")
                    load_forecasting(dataset, data_path)


if __name__ == "__main__":
    """
    Example simple usage, with arguments input via script or hard coded for testing.
    """
    print("Running download_datsets.py")
    check_and_download_datasets(sys.argv[1:])
